  );

-- SBA tests: read own; admin all; sub_admin in subject/year scope
drop policy if exists "sba_tests_select" on public.sba_tests;
create policy "sba_tests_select"
  on public.sba_tests for select
  using (
    created_by = auth.uid()
    or exists (select 1 from public.profiles p where p.id = auth.uid() and p.role = 'admin')
    or (
      exists (select 1 from public.profiles p where p.id = auth.uid() and p.role = 'sub_admin')
      and (
        exists (
          select 1
          from public.committee_members cm
          join public.committees c on c.id = cm.committee_id
          where cm.profile_id = auth.uid()
            and (c.subject is null or c.subject = sba_tests.subject)
            and (c.test_year is null or c.test_year = sba_tests.test_year)
        )
      )
    )
  );

drop policy if exists "sba_tests_insert" on public.sba_tests;
create policy "sba_tests_insert"
  on public.sba_tests for insert
  with check (
    created_by = auth.uid()
    and exists (
      select 1 from public.profiles p
      where p.id = auth.uid() and p.role in ('educator', 'admin', 'sub_admin')
    )
  );

drop policy if exists "sba_tests_update" on public.sba_tests;
create policy "sba_tests_update"
  on public.sba_tests for update
  using (
    (created_by = auth.uid() and exists (
      select 1 from public.profiles p where p.id = auth.uid() and p.role in ('educator', 'admin', 'sub_admin')
    ))
    or exists (select 1 from public.profiles p where p.id = auth.uid() and p.role = 'admin')
    or (
      exists (select 1 from public.profiles p where p.id = auth.uid() and p.role = 'sub_admin')
      and (
        exists (
          select 1
          from public.committee_members cm
          where cm.committee_id = sba_tests.committee_id
            and cm.profile_id = auth.uid()
        )
      )
    )
  );

