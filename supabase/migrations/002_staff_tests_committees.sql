-- Run after initial schema. Safe to re-run: uses IF NOT EXISTS / exception handling where needed.

-- -----------------------------------------------------------------------------
-- Extend user role (PostgreSQL: add enum value; ignore if exists)
-- -----------------------------------------------------------------------------
do $$
begin
  if not exists (
    select 1 from pg_enum e
    join pg_type t on e.enumtypid = t.oid
    where t.typname = 'user_role' and e.enumlabel = 'sub_admin'
  ) then
    alter type public.user_role add value 'sub_admin';
  end if;
end
$$;

-- -----------------------------------------------------------------------------
-- Enums
-- -----------------------------------------------------------------------------
do $$
begin
  if not exists (select 1 from pg_type where typname = 'test_review_status') then
    create type public.test_review_status as enum (
      'pending_committee',
      'approved',
      'rejected'
    );
  end if;
end
$$;

-- -----------------------------------------------------------------------------
-- Departments (choose which department the test is for)
-- -----------------------------------------------------------------------------
create table if not exists public.departments (
  id uuid primary key default gen_random_uuid(),
  name text not null unique,
  updated_at timestamptz not null default now()
);

create table if not exists public.committees (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  subject text,
  test_year int,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists committees_subject_idx on public.committees (subject);
create index if not exists committees_year_idx on public.committees (test_year);

create table if not exists public.committee_members (
  committee_id uuid not null references public.committees (id) on delete cascade,
  profile_id uuid not null references public.profiles (id) on delete cascade,
  primary key (committee_id, profile_id)
);

-- -----------------------------------------------------------------------------
-- SBA test (parent) + questions (UUID PK per question, generated by Supabase)
-- -----------------------------------------------------------------------------
create table if not exists public.sba_tests (
  id uuid primary key default gen_random_uuid(),
  subject text not null,
  subject_code text not null,
  department_id uuid references public.departments (id) on delete set null,
  committee_id uuid references public.committees (id) on delete set null,
  review_status public.test_review_status not null default 'pending_committee',
  test_year int not null default (extract(year from now())::int),
  created_by uuid references public.profiles (id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists sba_tests_subject_idx on public.sba_tests (subject);
create index if not exists sba_tests_year_idx on public.sba_tests (test_year);
create index if not exists sba_tests_created_by_idx on public.sba_tests (created_by);
create index if not exists sba_tests_review_idx on public.sba_tests (review_status);

-- options: jsonb e.g. [{"id":"A","text":"..."},...]; correct_option_id matches one id
create table if not exists public.sba_test_questions (
  id uuid primary key default gen_random_uuid(),
  sba_test_id uuid not null references public.sba_tests (id) on delete cascade,
  sequence_order int not null check (sequence_order >= 1),
  stem text not null,
  image_url text,
  options jsonb not null default '[]'::jsonb,
  correct_option_id text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint sba_test_questions_order_unique unique (sba_test_id, sequence_order)
);

create index if not exists sba_test_questions_test_idx on public.sba_test_questions (sba_test_id);

-- -----------------------------------------------------------------------------
-- MEQ test + stages (per-stage time limit and question; student types answers in app)
-- -----------------------------------------------------------------------------
create table if not exists public.meq_tests (
  id uuid primary key default gen_random_uuid(),
  subject text not null,
  subject_code text not null,
  department_id uuid references public.departments (id) on delete set null,
  committee_id uuid references public.committees (id) on delete set null,
  review_status public.test_review_status not null default 'pending_committee',
  test_year int not null default (extract(year from now())::int),
  time_limit_minutes int,
  first_page_stem text not null default '',
  vignette text not null,
  created_by uuid references public.profiles (id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create index if not exists meq_tests_subject_idx on public.meq_tests (subject);
create index if not exists meq_tests_year_idx on public.meq_tests (test_year);
create index if not exists meq_tests_created_by_idx on public.meq_tests (created_by);

create table if not exists public.meq_test_stages (
  id uuid primary key default gen_random_uuid(),
  meq_test_id uuid not null references public.meq_tests (id) on delete cascade,
  sequence_order int not null check (sequence_order >= 1),
  time_limit_minutes int,
  question_text text not null,
  media_urls text[] not null default '{}',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint meq_test_stages_order_unique unique (meq_test_id, sequence_order)
);

create index if not exists meq_test_stages_test_idx on public.meq_test_stages (meq_test_id);

